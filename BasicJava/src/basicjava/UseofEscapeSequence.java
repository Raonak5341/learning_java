package basicjava;

public class UseofEscapeSequence {
	
	public static void main(String[] args) {
		//\n used to print new line
		System.out.println("Raonak Jahan \n017223344 \nBsc in CSE");
		//\t used to print a tab
		System.out.println("1 \t 2");
		// \" used to print double quotation
		System.out.println("\"Java Programming\" ");
		
	}

}
